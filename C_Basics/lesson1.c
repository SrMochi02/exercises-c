#include <stdio.h>

int main () {

    //Uses %d for print integer values
    printf("\nint size value in bytes is:%d", sizeof(int));
    //Uses %c for characters values
    printf("\nchar size value in bytes is:%c", sizeof(char));
    
    //uses %f for print floating point values 
    printf("\nfloat size value in bytes is:%f", sizeof(float));
    printf("\ndouble size value in bytes is:%f", sizeof(double));
    printf("\n"); 


}
